#include <iostream>   // This library is used for input and output
using namespace std;  // Allows us to use cout and cin without std::

int main()            // Main function where the program starts
{
    int i = 1;        // Declare a variable i and initialize it with 1

    while (i <= 10)   // While loop runs as long as i is less than or equal to 10
    {
        cout << i << endl;  // Display the value of i on the screen
        i ++;                // Increase the value of i by 1
    }

    return 0;        // End of program
}